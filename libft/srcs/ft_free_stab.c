#include "../inc/libft.h"

void	ft_free_stab(char **str)
{
	int	i;

	i = 0;
	if (**str && *str && str)
	{
		while (str[i] != 0)
		{
			free(str[i]);
			i++;
		}
		free(str);
	}
}
